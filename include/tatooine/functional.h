#ifndef TATOOINE_FUNCTIONAL_H
#define TATOOINE_FUNCTIONAL_H
//==============================================================================
#include <functional>
//==============================================================================
namespace tatooine {
//==============================================================================
constexpr auto invoke(auto&&... funcs) {
  (std::invoke(std::forward<decltype(funcs)>(funcs)), ...);
}
//==============================================================================
}  // namespace tatooine
//==============================================================================
#endif
